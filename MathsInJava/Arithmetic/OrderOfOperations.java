package com.example.ArithmeticExpressions.ArithmeticExpressions;

public class OrderOfOperations {
    public static void main(String[] args) {

        int x = 10 + 3 * 2;
        System.out.println(x); // outputs 16

        int y = (10 + 3) * 2;
        System.out.println(y); // outputs 26

        // Order: BIDMAS
        //  ()
        //  */
        //  +-
    }
}
