package Beginner.Iteration;

public class ForLoops {

    public static void main(String[] args) {
        for(int i = 0; i <= 5; i++)
            System.out.println("Hello World" + i); // outputs HW 0, HW 1 etc

        for(int i = 5; i > 0; i--)
            System.out.println("Hello World" + i); // outputs HW 5, HW 4 etc
    }
}
