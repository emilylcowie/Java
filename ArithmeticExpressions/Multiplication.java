package com.example.ArithmeticExpressions.ArithmeticExpressions;

public class Multiplication {
    public static void main(String[] args) {
        int result = 10 * 3;
        System.out.println(result);

        //compound assignments
        int x = 1;
        x *= 2; //also x = x * 2
        System.out.println(x);
    }
}
